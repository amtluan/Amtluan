boolean checkWord(String s) {
        if (s.equals("Co Van")) {
            return false;
        }
        Set<String> keys = vocabulary.keySet();;
        if (keys.contains(s)) {
            return false;
        }
        return true;

    }
    private void myTreeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_myTreeMouseClicked
        TreePath path = myTree.getSelectionPath();
        if (path != null) {
            DefaultMutableTreeNode clickedNode = (DefaultMutableTreeNode) path.getLastPathComponent();
            // lay data cua  node click
            String s = (String) clickedNode.getUserObject();
            if (checkWord(s) == true) {
                txtWord.setText(s);
            } else {
                txtWord.setText("");
            }

        }
    }//GEN-LAST:event_myTreeMouseClicked

