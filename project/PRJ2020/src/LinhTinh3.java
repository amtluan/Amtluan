    public void LoadDataToTree() {
        root = new DefaultMutableTreeNode("Co Van");
        DefaultTreeModel model = (DefaultTreeModel) myTree.getModel();

        model.setRoot(root);
        Set<String> keys = vocabulary.keySet();
        for (String k : keys) {
            DefaultMutableTreeNode node = new DefaultMutableTreeNode(k);
            root.add(node);
            ArrayList<String> List = vocabulary.get(k);
            for (String w : List) {
                DefaultMutableTreeNode childNode = new DefaultMutableTreeNode(w);
                node.add(childNode);

            }
        }
    }

